    {
        /// <summary>
        /// A pub serving drinks and often food.
        /// </summary>
        public const string Pub = "pub";

        /// <summary>
        /// A bar focused on drinks and socializing.
        /// </summary>
        public const string Bar = "bar";

        /// <summary>
        /// A restaurant serving prepared meals.
        /// </summary>
        public const string Restaurant = "restaurant";

        /// <summary>
        /// A nightclub with late-night entertainment.
        /// </summary>
        public const string Nightclub = "nightclub";

        /// <summary>
        /// A cafe serving coffee and light meals.
        /// </summary>
        public const string Cafe = "cafe";

        /// <summary>
        /// A brewery producing and serving beer.
        /// </summary>
        public const string Brewery = "brewery";

        /// <summary>
        /// A winery producing and serving wine.
        /// </summary>
        public const string Winery = "winery";

        /// <summary>
        /// A distillery producing and serving spirits.
        /// </summary>
        public const string Distillery = "distillery";

        /// <summary>
        /// A sports bar with a focus on televised sports.
        /// </summary>
        public const string SportsBar = "sports_bar";

        /// <summary>
        /// A comedy club for stand-up or improv performances.
        /// </summary>
        public const string ComedyClub = "comedy_club";
    }
}
